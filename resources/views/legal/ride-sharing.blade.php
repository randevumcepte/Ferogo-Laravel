<p>
    Ferxgo, Türkiye'de hızla büyüyen <strong>paylaşımlı yolculuk</strong> (ride-sharing) modelinin dijital platformu olarak
    faaliyet gösterir. Bu sayfa, paylaşımlı yolculuğun ne olduğunu ve Ferxgo'nun bu modeldeki rolünü açıklar.
</p>

<h2>Paylaşımlı Yolculuk Nedir?</h2>
<p>
    Paylaşımlı yolculuk, kendi güzergahında seyahat eden bir kişinin (üye sürücü), aynı güzergaha gitmek isteyen bir veya birden fazla
    yolcuyu aracına alarak <strong>yolculuk maliyetini paylaşması</strong> esasına dayanan bir ulaşım modelidir.
</p>

<h2>Geleneksel Taşımacılıktan Farkı</h2>

<h3>Geleneksel Ticari Taşımacılık</h3>
<ul>
    <li>Sürücü, mesleğini icra etmek için yola çıkar</li>
    <li>Müşteri için özel olarak araç tahsis edilir</li>
    <li>Sabit tarife veya taksimetre üzerinden ücret alınır</li>
    <li>Ticari plaka, taksi durağı ve yetki belgesi gerektirir</li>
</ul>

<h3>Paylaşımlı Yolculuk</h3>
<ul>
    <li>Üye sürücü, kendi planlı güzergahında zaten yolculuk yapmaktadır</li>
    <li>Yolcu, mevcut güzergahın paylaşımcısıdır</li>
    <li>Yolcu, yolculuğun değişken giderlerine (yakıt, amortisman) <strong>katkı payı</strong> öder</li>
    <li>Ticari taşımacılık yetki belgesi gerekmez</li>
</ul>

<h2>Yasal Dayanak</h2>
<p>
    Paylaşımlı yolculuk modeli, Türkiye'de mahkeme kararları ile pekişmiş bir hukuki temele sahiptir:
</p>
<ul>
    <li><strong>İstanbul Bölge Adliye Mahkemesi 14. Hukuk Dairesi:</strong> Paylaşımlı yolculuk hizmetinin "korsan taşımacılık" kapsamına girmediğine karar verilmiştir.</li>
    <li><strong>Danıştay (1 Kasım 2025):</strong> Paylaşımlı yolculuk platformlarının elektronik ulaşım yönetim lisansı alma haklarını kesinleştirmiştir.</li>
    <li><strong>Antalya 2. İdare Mahkemesi:</strong> Paylaşımlı yolculuk kullanıcılarına verilen idari para cezalarının iptaline karar vermiştir.</li>
    <li><strong>Gelir İdaresi Başkanlığı (7 Ağustos 2024):</strong> Paylaşımlı yolculuk gelirinin ticari kazanç olduğunu ve <strong>üye sürücüye ait</strong> olduğunu duyurmuştur.</li>
</ul>

