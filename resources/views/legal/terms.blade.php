<p>
    Ferxgo'ya hoş geldiniz. Bu metin, Ferxgo dijital paylaşımlı yolculuk platformunun ("Platform") kullanım koşullarını düzenler.
    Platforma erişim sağlayan, hesap oluşturan veya hizmetlerden faydalanan tüm kullanıcılar bu koşulları kabul etmiş sayılır.
</p>

<h2>1. Platformun Niteliği</h2>
<p>
    Ferxgo, kendi güzergahlarını paylaşmak isteyen bağımsız üye sürücüler ile aynı güzergahta seyahat etmek isteyen yolcuları
    dijital ortamda buluşturan bir <strong>paylaşımlı yolculuk koordinasyon platformudur</strong>. Ferxgo, 6563 sayılı Elektronik
    Ticaretin Düzenlenmesi Hakkında Kanun kapsamında <strong>aracı hizmet sağlayıcı</strong> sıfatıyla faaliyet gösterir.
</p>
<p>
    Ferxgo:
</p>
<ul>
    <li>Ticari yolcu taşımacılığı hizmeti <strong>sağlamaz</strong>.</li>
    <li>Yolculuk hizmetinin tarafı <strong>değildir</strong>.</li>
    <li>Üye sürücülerin işvereni, ortağı veya temsilcisi <strong>değildir</strong>.</li>
    <li>Yolculuk sırasında oluşabilecek zarar, gecikme veya iptal durumlarında sorumlu tutulamaz.</li>
</ul>

<h2>2. Tanımlar</h2>
<ul>
    <li><strong>Üye Sürücü:</strong> Kendi aracıyla, kendi güzergahında, kendi takdiriyle paylaşımlı yolculuk yapmayı kabul eden bağımsız vergi mükellefi gerçek kişi.</li>
    <li><strong>Yolcu:</strong> Üye sürücünün güzergahında ortak yolculuk yapmak isteyen kişi.</li>
    <li><strong>Katkı Payı:</strong> Yolcunun üye sürücüye, yolculuğun değişken giderlerine katkı olarak ödediği tutar.</li>
    <li><strong>Üyelik Bedeli:</strong> Üye sürücünün platform hizmetlerinden yararlanabilmek için Ferxgo'ya ödediği dijital platform hizmet bedeli.</li>
    <li><strong>Eşleştirme:</strong> Ferxgo'nun yolcu ile üye sürücüyü algoritma yoluyla buluşturma işlemi.</li>
</ul>

<h2>3. Üye Sürücünün Yükümlülükleri</h2>
<ul>
    <li>Üye sürücü, paylaşımlı yolculuk faaliyetinden elde ettiği gelir nedeniyle <strong>kendi adına vergi mükellefidir</strong>. Vergisel sorumluluk üye sürücüye aittir.</li>
    <li>Üye sürücü, Gelir İdaresi Başkanlığı'nın 7 Ağustos 2024 tarihli kararı uyarınca gelirini ticari kazanç olarak beyan etmekle yükümlüdür.</li>
    <li>Üye sürücü, ehliyetinin geçerli olduğunu, adli sicilinin temiz olduğunu beyan eder.</li>
    <li>Üye sürücü, kendi çalışma saatlerini, güzergahını ve yolcu kabul kararını <strong>tamamen kendi takdiriyle</strong> belirler. Ferxgo, çalışma saati, miktar veya performans hedefi dayatmaz.</li>
    <li>Üye sürücü, yolcudan tahsil ettiği katkı payı için yasal mevzuata uygun belge (e-arşiv fatura veya yazar kasa fişi) düzenlemekle yükümlüdür.</li>
</ul>

<h2>4. Yolcunun Yükümlülükleri</h2>
<ul>
    <li>Yolcu, platform üzerinden yaptığı taleplerde gerçek bilgilerini paylaşır.</li>
    <li>Yolcu, yolculuğa zamanında hazır bulunur; aksi halde üye sürücünün boşa gitmesi sebebiyle <em>no-show</em> ücreti uygulanabilir.</li>
    <li>Yolcu, üye sürücüye karşı saygılı davranır; araç içinde yasalara aykırı eylemde bulunmaz.</li>
    <li>Yolcu, yolculuk sırasında oluşacak kişisel zararlardan veya eşya kaybından Ferxgo'yu sorumlu tutamaz.</li>
</ul>

<h2>5. Ödeme &amp; Katkı Payı</h2>
<p>
    Yolculuk için yolcu tarafından ödenen <strong>katkı payı doğrudan üye sürücüye aittir</strong>. Ferxgo bu ödemenin tarafı değildir;
    tahsilat üye sürücünün kendi imkanları (nakit, kart, dijital cüzdan) üzerinden gerçekleşir veya platformun anlaşmalı ödeme altyapısı üzerinden
    doğrudan üye sürücünün hesabına aktarılır.
</p>
<p>
    Üye sürücü, platform üzerinden faydalandığı dijital eşleştirme, harita, bildirim, destek ve analiz hizmetleri karşılığında
    Ferxgo'ya <strong>üyelik bedeli</strong> öder. Bu bedel, yolculuk başına alınan komisyon <strong>değildir</strong>; sabit dönemsel bir dijital hizmet ücretidir.
</p>

<h2>6. Üyelik İptali, Cayma Hakkı</h2>
<p>
    Üye sürücü, satın aldığı dijital üyelik paketi için 6502 sayılı Tüketicinin Korunması Hakkında Kanun kapsamında
    <strong>14 gün cayma hakkına</strong> sahiptir. Ancak hizmetin tüketildiği kısım için iade söz konusu olmayabilir.
</p>

<h2>7. Platform Hizmetinin Askıya Alınması</h2>
<p>
    Ferxgo, aşağıdaki hallerde üyeliği veya kullanıcı hesabını geçici ya da kalıcı olarak askıya alma hakkını saklı tutar:
</p>
<ul>
    <li>Hizmet şartlarına aykırı davranış</li>
    <li>Sahte bilgi paylaşımı, kimlik sahteciliği</li>
    <li>Yolcu veya üye sürücüye karşı yapılan sürekli şikayetler</li>
    <li>Yasalara aykırı kullanım</li>
</ul>

<h2>8. Sorumluluk Sınırlaması</h2>
<p>
    Ferxgo'nun yolculukla ilgili oluşabilecek doğrudan veya dolaylı zararlardan, kazalardan, gecikmelerden, hizmet aksaklıklarından
    sorumluluğu yoktur. Yolculuk sırasında oluşacak hukuki uyuşmazlıklar yolcu ile üye sürücü arasında çözülür.
</p>

