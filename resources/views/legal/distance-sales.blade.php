    <li>Bildirim ve iletişim altyapısı</li>
    <li>Yolculuk geçmişi ve analiz</li>
    <li>Müşteri destek erişimi</li>
</ul>

<h2>3. Ücret &amp; Ödeme</h2>
<p>
    Üyelik paketi bedeli, paket türüne göre değişir ve satın alma sırasında ekranda KDV dahil olarak gösterilir.
    Ödeme, kayıtlı kredi/banka kartı üzerinden anlık olarak gerçekleştirilir. Ödeme bilgileri 3D Secure ile korunur ve PCI-DSS uyumlu
    ödeme altyapısı sağlayıcıları aracılığıyla işlenir; Ferxgo kart bilgilerini saklamaz.
</p>

<h2>4. Hizmetin İfası</h2>
<p>
    Üyelik paketi, ödeme onayını takiben anında aktif edilir ve seçilen süre boyunca (günlük/haftalık/aylık) Alıcı'nın
    kullanımına açıktır. Hizmet kesintisiz olarak sunulmaya çalışılır; planlı bakımlar önceden duyurulur.
</p>

<h2>5. Cayma Hakkı</h2>
<p>
    Alıcı, üyelik paketi satın alma tarihinden itibaren <strong>14 (on dört) gün içinde</strong> hiçbir gerekçe göstermeksizin
    cayma hakkına sahiptir. Cayma hakkı, hizmetin tüketildiği kısım için geçerli değildir.
</p>
